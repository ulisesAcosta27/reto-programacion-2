const dia = parseInt(prompt('Escriba el dia'));
const mes = parseInt(prompt('Escriba el mes'));
const a単o = parseInt(prompt('Escriba el a単o'));


if(dia === 25 && mes === 12){
    alert(`${dia}/${mes}/${a単o} felicidades hoy es navidad`)
}else{
    alert(`${dia}/${mes}/${a単o} hoy no es navidad :(`)
}